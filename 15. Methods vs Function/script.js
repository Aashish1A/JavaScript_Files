const maths = {
    add: function (a,b){
        return a+b;
    },
    sub: function (c,d){
        return c-d;
    },
    square: function (e){
        return e*e
    },

    // another way 
    Cube(a){
        return a*a*a;
    }
}

console.log(`The sum of no is : ${maths.add(4, 5)}`);
console.log(`The subtract is : ${maths.sub(14, 5)}`);
console.log(`The square is : ${maths.square(4)}`);
console.log(`The Cube is : ${maths.Cube(4)}`);




